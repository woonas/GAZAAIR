package kr.gaza.booking;

public class CompanionVO {
    private int companionNum;
    private int peopleNum;
    private String companionName;
    private String companionBirth;
    private int isDisabled;

    public int getCompanionNum() {
        return companionNum;
    }

    public void setCompanionNum(int companionNum) {
        this.companionNum = companionNum;
    }

    public int getPeopleNum() {
        return peopleNum;
    }

    public void setPeopleNum(int peopleNum) {
        this.peopleNum = peopleNum;
    }

    public String getCompanionName() {
        return companionName;
    }

    public void setCompanionName(String companionName) {
        this.companionName = companionName;
    }

    public String getCompanionBirth() {
        return companionBirth;
    }

    public void setCompanionBirth(String companionBirth) {
        this.companionBirth = companionBirth;
    }

    public int getIsDisabled() {
        return isDisabled;
    }

    public void setIsDisabled(int isDisabled) {
        this.isDisabled = isDisabled;
    }
}
